/**
 * 
 * Point class will act as a pointer
 * for the location of the trainer
 * on the 2D array
 *
 */
public class Point {
   public int x;
   public int y;
   
   /**
    * default constructor for point
    * @param x - Takes in the row number
    * @param y - Takes in the column number
    */
   public Point(int x, int y){
      this.x = x;
      this.y =y;
   }
   
}
